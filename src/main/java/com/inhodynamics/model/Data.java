package com.inhodynamics.model;

public class Data {
	String error;
	String result;
	
	public Data() {
		super();
	}

	public Data(String error, String result) {
		super();
		this.error = error;
		this.result = result;
	}

	public String getError() {
		return error;
	}

	public String getResult() {
		return result;
	}
	
	
	
}
